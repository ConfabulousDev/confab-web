function FilterSidebar({
  counts,
  visibleCategories,
  onToggleCategory,
  collapsed,
  onToggleCollapse,
}: FilterSidebarProps) {
  return (
    <PageSidebar
      title="Message Filters"
      collapsed={collapsed}
      onToggleCollapse={onToggleCollapse}
      collapsible={true}
    >
      {FILTER_ITEMS.map((item) => {
        const count = counts[item.category];
        const isVisible = visibleCategories.has(item.category);
        const isDisabled = count === 0;

        return (
          <SidebarItem
            key={item.category}
            icon={item.icon}
            label={item.label}
            count={count}
            active={isVisible}
            disabled={isDisabled}
            onClick={() => onToggleCategory(item.category)}
            collapsed={collapsed}
            activeColor={item.activeColor}
          />
        );
      })}
    </PageSidebar>
  );
}

